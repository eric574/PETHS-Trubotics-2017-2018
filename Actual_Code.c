task usercontrol() {
	// const double deviation = 2.0; (constant has already been statically-defined at beginning...)
	while (1) {
		/*
		<<Drive Control>>
		*/
		motor[left1] = vexRT[Ch3];
		motor[left2]= vexRT[Ch3];
		motor[right1] = vexRT[Ch2]/deviation;
		motor[right2] = vexRT[Ch2]/deviation;

		/*
		<<Claw Control>>
		*/
		if (vexRT[Btn8L]) { // Opens the claw
			motor[claw] = 30;
		}
		else if (vexRT[Btn8R]) { // Closes the claw
			motor[claw] = -60;
			wait1Msec(220); // Wait 220 milliseconds
			motor[claw] = 0;
		}

		/*
		<<Claw Arm>>
		*/
		if (vexRT[Btn6U] == 1) {
			motor[leftArm3] = -100;
			motor[rightArm3]= -100;
			motor[clawangle] = -80;
		}
		else if (vexRT[Btn6D] == 1) {
			motor[leftArm3] = 100;
			motor[rightArm3] = 100;
			motor[clawangle] = 80;
		}
		else {
			motor[leftArm3] = 0;
			motor[rightArm3] = 0;
			motor[clawangle] = 0;
		}

		/*
		<<Mobile Goal Lift>>
		*/
		if (vexRT[Btn5U]) {
			motor[mobileLiftL] = motor[mobileLiftR] = -70;
		}
		else if (vexRT[Btn5D]) {
			motor[mobileLiftR] = motor[mobileLiftL] = 70;
		}
		else {
			/*
			<<Mobile Goal Automatic Adjustment>>
			*/
			if (vexRT[Btn7R] == 1) { // Press Button 7R to automatically adjust mobile-goal
				Set_Mobile_Goal_Height();
			}
			else {
				motor[mobileLiftR] = motor[mobileLiftL] = 0;
			}
		}

		/*
		<<Claw Angle>>
		*/
		if (vexRT[Btn7U] == 1) {
			motor[clawangle] = -75;
		}
		else if (vexRT[Btn7D] == 1) {
			motor[clawangle] = 75;
		}
	}
}
