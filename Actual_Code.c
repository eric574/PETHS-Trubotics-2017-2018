int cone = 0, mobilegoal = 0; // Statically defined variables cone - 0 is for down action and 1 is for up action and same for mobilegoal
const double deviation = 2.0; // UPDATE: STILL NEED TO CHANGE VALUE TO AVOID DEVIATION DURING AUTONOMOUS

void DriveU (int amount) {
	motor[left1] = amount;
	motor[left2] = amount;
	motor[right1] = amount / deviation ;
	motor[right2] = amount / deviation;
}

void DriveD (int amount) {
	motor[left1] = -amount;
	motor[left2] = -amount;
	motor[right1] = -amount / deviation;
	motor[right2] = -amount / deviation;
}

void TurnR (int amount) {
	motor[left1] = amount;
	motor[left2] = amount;
	motor[right1] = -amount / deviation;
	motor[right2] = -amount / deviation;
}

void TurnL (int amount) {
	motor[left1] = -amount;
	motor[left2] = -amount;
	motor[right1] = amount / deviation;
	motor[right2] = amount / deviation;
}

// Opens the claw (left button on the right is to open)
void ClawU (int amount) {
	motor[claw] = -amount;
	cone = 1;
}

// Closes the claw (right button on the right is to close)
void ClawD (int amount) {
	motor[claw] = amount;
	cone = 0;
}

void StopMobileLift () {
	motor[mobileLiftL] = motor[mobileLiftR] = 0;
}

void MobileGoalU (int amount) {
	motor[mobileLiftL] = motor[mobileLiftR] = -amount;
	mobilegoal = 1;
}

void MobileGoalD (int amount) {
	motor[mobileLiftL] = motor[mobileLiftR] = amount;
	mobilegoal = 0;
}

void StopArmLift () { // Have to reset the arm motor values each time
	motor[leftArm3] = motor[rightArm3] = motor[clawangle] = 0;
}

void LiftArmU (int amount) { // Move the arm backwards
	motor[leftArm3] = -amount;
	motor[rightArm3]= -amount;
	motor[clawangle] = -75;
}

void LiftArmD (int amount) { // Move the arm forwards
	motor[leftArm3] = amount;
	motor[rightArm3]= amount;
	motor[clawangle] = 75;
}

void SetClawAngle (int amount) {
	motor[clawangle] = -amount;
}

void KeepMobileGoalU (int amount) {
	if (mobilegoal == 0) motor[mobileLiftL] = motor[mobileLiftR] = 0;
	else motor[mobileLiftL] = motor[mobileLiftR] = -amount;
}

void KeepConeU (int amount) {
	if (mobilegoal == 0) motor[mobileLiftL] = motor[mobileLiftR] = 0;
	else motor[mobileLiftL] = motor[mobileLiftR] = -amount;
}

void StopClawAngle () {
	motor[clawangle] = 0;
}

void StopClaw () {
	motor[claw] = 0;
}

void StopDrive () {
	motor[left1] = 0;
	motor[left2] = 0;
	motor[right1] = 0;
	motor[right2] = 0;
}

